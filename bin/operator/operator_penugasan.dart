void main(){
  //untuk operator =
  int angka;
  angka = 30;
  print(angka);

//untuk operator +=
  int a,b;
  a = 10;
  b = 40;
  a += b;
  print(a);

  //untuk operator -=
  int c,d;
  c = 10;
  d = 40;
  c -= d;
  print(c);

  //untuk operator *=
  int e,f;
  e = 10;
  f = 40;
  e += f;
  print(e);

  //untuk operator /=
  int g,h;
  g = 10;
  h = 40;
  g += h;
  print(g);

//untuk operator ~/=
  int i,j;
  i = 10;
  j = 40;
  i += j;
  print(i);

//untuk operator %=
  int k,l;
  k = 10;
  l = 40;
  k += l;
  print(k);
}